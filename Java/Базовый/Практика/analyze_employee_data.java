/**
 * Задача: Анализ данных сотрудников
 *
 * Вы работаете над модулем внутрикорпоративной системы учета данных сотрудников компании. Программа должна:
 * 1. Найти сотрудника с минимальным возрастом;
 * 2. Вычислить медианный возраст всех сотрудников;
 * 3. Найти сотрудника с максимальным возрастом.
 * Медиана — число, которое находится в середине этого набора, если его упорядочить по возрастанию.
 * Если набор чисел четный, то берется среднее между двумя средними числами и округляется до целого.
 *
 * Формат ввода:
 * Одна строка — данные сотрудников. Данные включают имя, возраст и отдел, разделенные запятой, без пробелов.
 * Информация о каждом сотруднике разделена через точку с запятой.
 * Количество сотрудников — от 2 до 100.
 *
 * Формат вывода:
 * Одна строка содержит три целых числа, разделенные пробелом: минимальный возраст, медианный возраст и максимальный возраст.
 *
 * Пример 1:
 * Входные данные: Иван,28,Инженер;Олег,34,HR;Денис,45,Маркетинг;Анна,30,Инженер;Борис,24,HR
 * Выходные данные: 24 30 45
 *
 * Пример 2:
 * Входные данные: Павел,28,Инженер;Елена,34,Маркетинг
 * Выходные данные: 28 31 34
 */

import java.util.Arrays;

public class AgeProcessor {

    public String processEmployeeData(String input) {
        // Разделяем входные данные по сотрудникам
        String[] employees = input.split(";");

        // Создаем массив для хранения возрастов
        int[] ages = new int[employees.length];

        // Извлекаем возраст каждого сотрудника и сохраняем в массив
        for (int i = 0; i < employees.length; i++) {
            String[] employeeData = employees[i].split(",");
            ages[i] = Integer.parseInt(employeeData[1]);
        }

        // Сортируем массив возрастов
        Arrays.sort(ages);

        // Находим минимальный возраст
        int minAge = ages[0];

        // Находим максимальный возраст
        int maxAge = ages[ages.length - 1];

        // Вычисляем медианный возраст
        int medianAge;
        int middle = ages.length / 2;
        if (ages.length % 2 == 0) {
            medianAge = (ages[middle - 1] + ages[middle]) / 2;
        } else {
            medianAge = ages[middle];
        }

        // Возвращаем результат в требуемом формате
        return minAge + " " + medianAge + " " + maxAge;
    }

    public static void main(String[] args) {
        AgeProcessor processor = new AgeProcessor();
        System.out.println(processor.processEmployeeData("Иван,28,Инженер;Олег,34,HR;Денис,45,Маркетинг;Анна,30,Инженер;Борис,24,HR")); // 24 30 45
        System.out.println(processor.processEmployeeData("Павел,28,Инженер;Елена,34,Маркетинг")); // 28 31 34
    }
}

