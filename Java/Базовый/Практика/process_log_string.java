/**
 * Задача: Обработка строк для системы анализа логов
 *
 * Вы работаете над системой анализа логов. Вам нужно разработать компонент для обработки и анализа строк.
 * Этот компонент должен выполнять следующие операции:
 * 1. Переворот строки
 * 2. Удаление всех цифр из строки
 * 3. Замена всех пробелов на подчеркивания (_)
 * 4. Вставка последовательности LOG в начало строки.
 * Затем система должна выводить обработанную строку.
 *
 * Формат ввода:
 * Одна строка, состоящая из различных латинских буквенных и числовых символов, а также пробелов.
 * Длина строки — от 2 до 100 символов.
 *
 * Формат вывода:
 * Одна строка — исходная строка, к которой применили последовательность операций из условия задачи.
 *
 * Пример 1:
 * Входные данные: Hello World 123
 * Выходные данные: LOG_dlroW_olleH
 *
 * Пример 2:
 * Входные данные: 12345678
 * Выходные данные: LOG
 */

public class LogProcessor {

    public String processLine(String line) {
        // 1. Переворот строки
        String reversed = new StringBuilder(line).reverse().toString();

        // 2. Удаление всех цифр из строки
        String noDigits = reversed.replaceAll("\\d", "");

        // 3. Замена всех пробелов на подчеркивания (_)
        String underscores = noDigits.replaceAll("\\s", "_");

        // 4. Вставка последовательности LOG в начало строки
        String result = "LOG" + underscores;

        return result;
    }

    public static void main(String[] args) {
        LogProcessor processor = new LogProcessor();
        System.out.println(processor.processLine("Hello World 123")); // LOG_dlroW_olleH
        System.out.println(processor.processLine("12345678")); // LOG
    }
}

